#pragma once

/*────────────────────────────────────────────────────────────────────────────*/

#include <unistd.h>
#include <cerrno>

/*────────────────────────────────────────────────────────────────────────────*/

namespace nodepp { namespace os {
    
    uint cpus(){ return ::sysconf( _SC_NPROCESSORS_ONLN ); }
    
    /*─······································································─*/

    void exit( int err=0 ){ ::exit(err); }

    /*─······································································─*/

    uint pid(){ return ::getpid(); }

    /*─······································································─*/

    uint error(){ return errno; }

}}

